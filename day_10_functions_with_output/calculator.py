def add(n1, n2):
    return n1 + n2


def subtract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


operators = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}

def calculate(do_continue=True):
    print(logo)
    first_number = float(input("What's the first number? "))
    for key, _ in operators.items():
        print(key)

    while do_continue:
        chosen_operator = input("Pick an operation: ")
        chosen_operation = operators.get(chosen_operator, [])
        if not chosen_operation:
            return "Invalid Operator"
        
        second_number = float(input("What's the next number? "))
        result = chosen_operation(first_number, second_number)
        
        proceed = input(
            f"Type 'y' to continue calculating with {result}, or type 'n' to start a new calculation: "
        )
        
        if proceed == 'n':
            do_continue = False
        else: 
            first_number = result

    return 'Total at the end of all the calculation is {result}'


print(calculate())
